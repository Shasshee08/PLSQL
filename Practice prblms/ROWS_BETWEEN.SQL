CREATE TABLE TEST1
(
    ID INT,
    SALE_DATE DATE,
    SALES NUMBER(4)
    );
    
    INSERT INTO TEST1 VALUES(&ID,'&DATE',&SALES);
    
    SELECT * FROM TEST1;
    
    SELECT ID,SALE_DATE,SALES,SUM(SALES) OVER(ORDER BY SALE_DATE ROWS BETWEEN 1 PRECEDING AND 1 FOLLOWING ) AS TOTAL FROM TEST1;
    
    SELECT ID,SALE_DATE,SALES,SUM(SALES) OVER(ORDER BY SALE_DATE ROWS BETWEEN 2 PRECEDING AND 2 FOLLOWING) AS TOTAL FROM TEST1;
    
    SELECT ID,SALE_DATE,SALES,SUM(SALES) OVER(ORDER BY SALE_DATE ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS TOTAL FROM TEST1;
    
    SELECT ID, SALE_DATE, SALES,
       SUM(SALES) OVER (ORDER BY SALE_DATE) AS RUNNING_TOTAL
FROM TEST1;

    SELECT ID,SALE_DATE,SALES,DENSE_RANK() OVER(ORDER BY SALE_DATE ) AS RANKS FROM TEST1;
    
    SELECT ID,SALE_DATE,SALES,ROW_NUMBER() OVER(ORDER BY SALE_DATE ) AS RANKS FROM TEST1;
    
    SELECT ID,SALE_DATE,SALES,AVG(SALES) OVER(ORDER BY SALE_DATE ROWS BETWEEN 1 PRECEDING AND 1 FOLLOWING) AS TOTAL FROM TEST1;
