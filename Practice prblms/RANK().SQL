    REGION VARCHAR2(10),
    TOTALSALES NUMBER(5)
    );
    
    DESC SALES_1;
    
    INSERT INTO SALES_1 VALUES(&SALESID,'&SALESPERSOM','&REGION',&TOTALSALES);
    
    SELECT * FROM SALES_1;
    
   SELECT * FROM(SELECT SALESPERSON,RANK() OVER(ORDER BY TOTALSALES DESC) rank FROM SALES_1);
   
   SELECT * FROM(SELECT SALESPERSON,DENSE_RANK() OVER(ORDER BY TOTALSALES DESC) RANK FROM SALES_1);
   
