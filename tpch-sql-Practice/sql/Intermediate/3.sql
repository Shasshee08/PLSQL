SELECT * FROM CUSTOMER;

SELECT * FROM ORDERS;

--============
SELECT 
    C.C_CUSTKEY,
    C.C_NAME,
    COUNT(O.O_ORDERKEY) AS  TOTAL_ORDERS_1995
FROM CUSTOMER C
JOIN
    ORDERS O ON O.O_CUSTKEY=C.C_CUSTKEY
WHERE 
    O.O_ORDERDATE BETWEEN DATE '1995-01-01' and DATE '1995-12-31'
GROUP BY 
    C.C_CUSTKEY,C.C_NAME;
ORDER BY 
    TOTAL_ORDERS_1995 DESC;